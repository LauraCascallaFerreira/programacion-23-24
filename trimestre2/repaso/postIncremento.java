package trimestre2.repaso;

public class postIncremento {
    
    public static void main(String[] args) {
        int a=1;
        System.out.println(a++);
        int b=1;
        System.out.println(++b);
        System.out.println("Cuanto vale a: "+a);
        System.out.println("Cuanto vale b: "+b);
    }
}
